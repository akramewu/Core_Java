package loops;

public class WhileLoop
{

  public static void main(String[] args)
  {

    /*
     * while(condition) { //logic here //increment or decrement }
     * 
     */

    int count = 10;

    while (count < 20)
    {

      System.out.println("values are" + count);
      count++;
    }
  }

}
